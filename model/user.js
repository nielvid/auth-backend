const mongoose = require("mongoose");

const UserSchema =  mongoose.Schema;

const user = new UserSchema({
    username: {
    type: String,
    required: true,
    min: 6,
    max: 255,
  },
  email: {
    type: String,
    required: true,
    min: 6,
    max: 255,
  },
  password: {
    type: String,
    required: true,
    min: 6,
    max: 1024,
  },
   telephone: {
    type: String,
    required: true,
    min: 6,
    max: 1024,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

const users = mongoose.model("users", user);

module.exports = users;

