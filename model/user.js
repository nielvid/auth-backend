var mongoose = require('mongoose')


var UserSchema =  mongoose.Schema;

const user = new UserSchema({
    username: {
    type: String,
    required: true,
    min: 6,
    max: 255,
  },
  email: {
    type: String,
    required: true,
    min: 6,
    max: 255,
  },
  password: {
    type: String,
    required: true,
    min: 6,
    max: 1024,
  },
   tel: {
    type: String,
    required: true,
    min: 6,
    max: 1024,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

var users = mongoose.model('users', user)

module.exports = users;

